let valor;

console.log(typeof valor);

valor = 'Hola';
console.log(typeof valor);

valor = true;
console.log(typeof valor);

valor = 20;
console.log(typeof valor);

valor = '20';
console.log(typeof valor);

valor = null;
console.log(typeof valor);

valor = [1, 2, 3, 5, 6];
console.log(typeof valor);

valor = {
    nombre: 'Bayron',
    apellido: 'Ortiz'
};

console.log(typeof valor);

valor = new Date();
console.log(typeof valor);